    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ClasseMysql objBD = new ClasseMysql();
        
        objBD.conectarBD();
        
        String concatenar = "";
        
        try {
            ResultSet retornoBD =  objBD.consultarBD();
            
            while (retornoBD.next()) {                
                int id  = retornoBD.getInt("id");
                double cpuOcioso  = retornoBD.getDouble("cpuOcioso");
                double cpuFreq  = retornoBD.getDouble("cpuFreq");
                double ram  = retornoBD.getDouble("ram");
                double bytesEnviados = retornoBD.getDouble("bytesEnviados");
                int errosEnvio  = retornoBD.getInt("errosEnvio");
                
                concatenar += String.format
                    ("IdDado: %d \ncpuOcioso: %.2f \ncpuFreq: %.2f \nram: %.2f \nbytesEnviados: %.2f \nerrosEnvio: %d" , id, cpuOcioso,cpuFreq, ram, bytesEnviados, errosEnvio);
            }
            
            txaResultado.setText(concatenar);
            
        } catch (SQLException ex) {
            Logger.getLogger(TelaConsulta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

